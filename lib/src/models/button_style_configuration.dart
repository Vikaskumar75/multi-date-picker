part of '../multi_date_picker.dart';

class ButtonStyleConfiguration {
  final double? height;
  final double? width;
  final BoxDecoration? decoration;
  final String? buttonText;
  final Widget? child;
  final TextStyle? buttonTextStyle;
  final Alignment? alignment;

  const ButtonStyleConfiguration({
    this.height,
    this.width,
    this.decoration,
    this.buttonText,
    this.buttonTextStyle,
    this.child,
    this.alignment,
  });
}
